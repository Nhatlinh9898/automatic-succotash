// Animation categories
export const animationCategories = {
    presentation: presentationAnimations,
    gesture: gestureAnimations,
    movement: movementAnimations,
    emotional: emotionalAnimations
};

// Helper function to get animation by number
export function getAnimationById(id) {
    return allAnimationsPart49[id];
}

// Helper function to get animations by category
export function getAnimationsByCategory(category) {
    return animationCategories[category] || {};
}

// Helper function to apply all animations to a character
export function applyAllPart49Animations(character) {
    Object.values(allAnimationsPart49).forEach(anim => {
        anim.function(character);
    });
}

// Helper function to apply category animations to a character
export function applyCategoryAnimations(character, category) {
    const animations = getAnimationsByCategory(category);
    Object.values(animations).forEach(anim => {
        anim.function(character);
    });
}

